def set_doc_font(document):
    styles = document.styles
    for style_name in ["Normal", "Title", "Heading 1", "Heading 2", "Heading 3"]:
        style = styles[style_name]
        style.font.name = "微软雅黑"
        style._element.rPr.rFonts.set(qn("w:eastAsia"), "微软雅黑")
    styles["Normal"].font.size = Pt(10.5)


def add_docx_meta(document, meta_data):
    for key, value in meta_data.items():
        paragraph = document.add_paragraph(f"{key}: {value}")
        paragraph.style = document.styles["Normal"]


def add_rule_paragraph(document, text):
    paragraph = document.add_paragraph(text)
    paragraph.paragraph_format.space_after = Pt(4)
    paragraph.paragraph_format.line_spacing = 1.15


def write_docx(doc):
    document = Document()
    set_doc_font(document)
    section = document.sections[0]
    section.top_margin = Pt(54)
    section.bottom_margin = Pt(54)
    section.left_margin = Pt(54)
    section.right_margin = Pt(54)
    document.add_heading(doc["meta"]["title"], level=1)
    document.add_paragraph("---")
    add_docx_meta(document, doc["meta"])
    document.add_paragraph("---")
    for title, lines in doc["sections"]:
        document.add_heading(title, level=2)
        for line in lines:
            add_rule_paragraph(document, line)
    document.save(OUT_DIR / doc["filename"])


def register_pdf_font():
    candidates = [
        Path("C:/Windows/Fonts/NotoSansSC-VF.ttf"),
        Path("C:/Windows/Fonts/msyh.ttc"),
        Path("C:/Windows/Fonts/simhei.ttf"),
        Path("C:/Windows/Fonts/Noto Sans SC (TrueType).otf"),
    ]
    font_path = next((path for path in candidates if path.exists()), None)
    if font_path is None:
        raise RuntimeError("未找到可用中文字体")
    pdfmetrics.registerFont(TTFont("PolicyCN", str(font_path)))


def paragraph(text, style):
    return Paragraph(text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;"), style)


def write_pdf(doc):
    register_pdf_font()
    path = OUT_DIR / doc["filename"]
    base = getSampleStyleSheet()
    title_style = ParagraphStyle(
        "PolicyTitle",
        parent=base["Title"],
        fontName="PolicyCN",
        fontSize=18,
        leading=24,
        textColor=colors.HexColor("#111827"),
        wordWrap="CJK",
        spaceAfter=8,
    )
    meta_style = ParagraphStyle(
        "PolicyMeta",
        parent=base["Normal"],
        fontName="PolicyCN",
        fontSize=8.5,
        leading=12,
        textColor=colors.HexColor("#374151"),
        wordWrap="CJK",
    )
    heading_style = ParagraphStyle(
        "PolicyHeading",
        parent=base["Heading2"],
        fontName="PolicyCN",
        fontSize=13,
        leading=18,
        textColor=colors.HexColor("#111827"),
        wordWrap="CJK",
        spaceBefore=8,
        spaceAfter=4,
    )
    body_style = ParagraphStyle(
        "PolicyBody",
        parent=base["BodyText"],
        fontName="PolicyCN",
        fontSize=10,
        leading=15,
        textColor=colors.HexColor("#111827"),
        wordWrap="CJK",
        spaceAfter=4,
    )
    story = [paragraph(doc["meta"]["title"], title_style), paragraph("---", meta_style)]
    for key, value in doc["meta"].items():
        story.append(paragraph(f"{key}: {value}", meta_style))
    story.append(paragraph("---", meta_style))
    story.append(Spacer(1, 4 * mm))
    for title, lines in doc["sections"]:
        story.append(paragraph(title, heading_style))
        for line in lines:
            story.append(paragraph(line, body_style))
    SimpleDocTemplate(
        str(path),
        pagesize=A4,
        rightMargin=16 * mm,
        leftMargin=16 * mm,
        topMargin=16 * mm,
        bottomMargin=16 * mm,
        title=doc["meta"]["title"],
    ).build(story)


def main():
    for doc in DOCS:
        if doc["format"] == "docx":
            write_docx(doc)
        else:
            write_pdf(doc)
    print(f"generated {len(DOCS)} files in {OUT_DIR}")


if __name__ == "__main__":
    main()
